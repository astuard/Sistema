    private void botonBuscarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonBuscarMousePressed
        // BOTON BUSCAR  ESTUDIANTE
        String NombreProducto = textProducto.getText();
            
        boolean encontrado = false;
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        modelo.setRowCount(0);
        List<Productos> productosLista = productos.listado();
        for (Productos producto : productosLista) {
            if (NombreProducto.equals(producto.getNombreProducto())) {
                encontrado = true;
                Object[] fila = {
                    producto.getIdProducto(),
                    producto.getNombreProducto(),
                    producto.getPrecio(),  
                    producto.getCantidad(),    
                    producto.getCategoria(),   
                    
                };
                modelo.addRow(fila);
            }

            // Asignar el modelo al JTable
            jTable2.setModel(modelo);
        }
        
        if (encontrado == false) {
            JOptionPane.showMessageDialog(null, "No se encontro ningun estudiante con esa cÃ©dula", "Sin Resultados", JOptionPane.INFORMATION_MESSAGE);
        }
      
     

    }//GEN-LAST:event_botonBuscarMousePressed

