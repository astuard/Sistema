    private void llenarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        modelo.setRowCount(0);
        List<Productos> productosLista = ProductosControlBD.listado();
        for (Productos producto : productosLista) {
            Object[] fila = {
                producto.getIdProducto(),
                producto.getNombreProducto(),
                producto.getPrecio(),  
                producto.getCantidad(),    
                producto.getCategoria(),   
                   
            };
            modelo.addRow(fila);
        }

        // Asignar el modelo al JTable
        jTable2.setModel(modelo);    
    }
    

    private void eliminarFila() {
        int selectedRow = jTable2.getSelectedRow(); 
        if (selectedRow != -1) { 
            int respuesta = JOptionPane.showConfirmDialog(null, "Â¿Deseas Eliminar la fila seleccionada?", "ConfirmaciÃ³n", JOptionPane.YES_NO_OPTION);

            if (respuesta == JOptionPane.YES_OPTION) {
                int idProductos = (int) jTable2.getValueAt(selectedRow, 0);
                productos.eliminar(idProductos);
                llenarTabla();
            } 

        } else {
            JOptionPane.showMessageDialog(null, "Â¡Seleccione la fila que desea eliminar!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void modificarFila() {
        int selectedRow = jTable2.getSelectedRow(); 
        if (selectedRow != -1) { 
            int idProducto = (int) jTable2.getValueAt(selectedRow, 0);
            String NombreProducto= (String) jTable2.getValueAt(selectedRow, 1);
            float precio = Float.parseFloat(jTable2.getValueAt(selectedRow, 2).toString()); 
            int cantidad = Integer.parseInt(jTable2.getValueAt(selectedRow, 3).toString()); 
            String categoria = (String) jTable2.getValueAt(selectedRow, 4);

            EditarProducto ventanaModificar = new EditarProducto(idProducto, NombreProducto, precio,  cantidad , categoria);
   

        } else {
            JOptionPane.showMessageDialog(null, "Â¡Seleccione la fila que desea modificar!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

